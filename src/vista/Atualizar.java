    private void buscarProdutoPeloIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarProdutoPeloIdActionPerformed
        // TODO add your handling code here:
        
        
        campoNome.setEnabled(true);
        campoValor.setEnabled(true);
        campoQuantidade.setEnabled(true);
        campoGenero.setEnabled(true);   
        
        
        String id = campoId.getText();
        
        Conexao fabrica = new Conexao();
        JDBCProduto gerente = new JDBCProduto(fabrica.abrirConexao());
        
        Produto produto = gerente.listarProdutoEspecifico(id);
        
        fabrica.fecharConexao();
  
        String nomeBanco = produto.getNome();
        String valorBanco = Integer.toString(produto.getValor());
        String quantidadeBanco = Integer.toString(produto.getQuantidade());
        String generoBanco = produto.getGenero();
        
        campoNome.setText(nomeBanco);
        campoValor.setText(valorBanco);
        campoQuantidade.setText(quantidadeBanco);
        campoGenero.setText(generoBanco);
        
        
        
        
        
    }//GEN-LAST:event_buscarProdutoPeloIdActionPerformed

    private void botaoAtualizarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarProdutoActionPerformed
        // TODO add your handling code here:
        
        Conexao fabrica = new Conexao();
        JDBCProduto gerente = new JDBCProduto(fabrica.abrirConexao());
       
        int id = Integer.parseInt(campoId.getText());
        String nome = campoNome.getText();
        int valor = Integer.parseInt(campoValor.getText());
        int quantidade = Integer.parseInt(campoQuantidade.getText());
        String genero = campoGenero.getText();
        
        gerente.atualizarProduto(id, nome, valor, quantidade, genero);
        
    }//GEN-LAST:event_botaoAtualizarProdutoActionPerformed

