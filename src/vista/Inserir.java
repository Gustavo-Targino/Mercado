    private void botaoInserirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoInserirProdutoActionPerformed
        // TODO add your handling code here:
        
        String nome = campoNome.getText();
        
        int valor = Integer.parseInt(campoValor.getText());
        
        int quantidade = Integer.parseInt(campoQuantidade.getText());
        
        String genero = campoGenero.getText();
        
        Produto p = new Produto(nome, valor, quantidade, genero);
        
        //Persistindo
        Conexao fabrica = new Conexao();
        JDBCProduto gerente = new JDBCProduto(fabrica.abrirConexao());        
       
        gerente.inserirProduto(p);
        fabrica.fecharConexao();
        
        campoNome.setText("");
        campoValor.setText("");
        campoQuantidade.setText("");
        campoGenero.setText("");
        campoNome.requestFocus();
    }//GEN-LAST:event_botaoInserirProdutoActionPerformed

